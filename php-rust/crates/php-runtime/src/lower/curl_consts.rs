/// Look up a `CURL*` constant by exact (case-sensitive) name.
pub(super) fn curl_constant(name: &[u8]) -> Option<i64> {
    CURL_CONSTANTS
        .binary_search_by(|(n, _)| n.cmp(&name))
        .ok()
        .map(|i| CURL_CONSTANTS[i].1)
}

