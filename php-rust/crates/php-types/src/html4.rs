/// Encode direction: the first table name for `code`, if any.
pub fn name_for(code: u32) -> Option<&'static [u8]> {
    HTML4_ENTITIES.iter().find(|(_, c)| *c == code).map(|(n, _)| *n)
}

/// Decode direction: the code for an exact (case-sensitive) `name`, if any.
pub fn code_for(name: &[u8]) -> Option<u32> {
    HTML4_ENTITIES.iter().find(|(n, _)| *n == name).map(|(_, c)| *c)
}
